#include "libft.h"

int	ft_isalnum(int value)
{
	if ((65 <= value && value <= 90) || (97 <= value && value <= 122))
	{
		return (1);
	}
	else if (48 <= value && value <= 57)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
